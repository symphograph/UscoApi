25 и 26 марта Южно-Сахалинский камерный оркестр под управлением
художественного руководителя и главного дирижера оркестра Тиграна
Ахназаряна дал два концерта, посвященных творчеству великих
композиторов Вольфганга Амадеуса Моцарта и Петра Ильича Чайковского.
<br><br>
Слушателям, пришедшим в эти дни на концерты, представилась возможность
услышать премьерную программу, в которой прозвучали Симфония № 5
Моцарта и увертюра-фантазия Ромео и Джульетта Чайковского.
<br><br>
Чарующая музыка в исполнении оркестра. Публика, затаившая
дыхание, наблюдавшая за каждым движением дирижёрской палочки в руках
Тиграна Ахназаряна. Восхищённое молчание зала в непродолжительных
паузах. Длительные овации и крики «Браво!» - такими выдались прошедшие
концерты в экспозиционном зале Исторического парка «Россия – моя
история».
<br><br>
Один из концертов – 25 марта, выпал на профессиональный праздник
людей, связанных с искусством – Всероссийский День работника культуры.
В этот день Южно-Сахалинский камерный оркестр и всех присутствующих
на концерте, торжественно поздравил с праздником мэр города Южно-
Сахалинска Сергей Александрович Надсадин.
