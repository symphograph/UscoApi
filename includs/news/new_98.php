Южно-Сахалинский камерный оркестр на протяжении 23 лет своего
существования всегда радовал своих слушателей прекрасной музыкой, вот и
сейчас, в непростое и сложное для нашей страны время оркестр считает
своим долгом не лишать людей возможности соприкасаться с прекрасным и
таким образом поднимать настроение людям.
