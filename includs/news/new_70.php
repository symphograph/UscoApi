B рамках социального проекта
«Фестиваль национальных культур «Музыка разных народов»,
организованного Ассамблеей народов Сахалинской области, с 01 по 07
октября пройдут Дни армянской культуры в Сахалинской области, в этом
году посвященные 100-летию пяти великих армянских композиторов,
которые внесли значительный вклад в развитие мировой культуры: Арно
