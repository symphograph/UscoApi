сделано им в области инструментального джаза, музыкально-театральных
жанров; Александр Григорьевич Арутюня́н (композитор, пианист).
Народный артист СССР. 58 лет был художественным руководителем
Армянской филармонии. Музыке Арутюняна свойственно творческое
использование армянского народно-интонационного материала, его ладо-
ритмических особенностей; Лазарь (Газарос) Мартиросович Сарьян
(композитор); Арам Ильич Хачатурян (Хачатрян) (композитор, дирижёр);
Э́двард Михайлович Мирзоян (композитор).
