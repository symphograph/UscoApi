С 25 октября по 3 ноября 2021 года в Южно-Сахалинске прошел IV
Дальневосточный музыкальный фестиваль для детей и юношества «Звезды
Эвтерпы», основатель и художественный руководитель фестиваля Тигран
Ахназарян. Организатором Фестиваля выступило Муниципальное бюджетное
учреждение «Южно-Сахалинский камерный оркестр» при поддержке
Компании «ЭКСОН НЕФТЕГАЗ ЛИМИТЕД» (Сахалинская область).
