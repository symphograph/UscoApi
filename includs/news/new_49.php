То, что делала за роялем Ева Геворгян, не поддавалось никаким объяснениям. Мощный музыкальный талант, невероятное
трудолюбие, лучшие учителя и оркестры, окружающие молодого исполнителя, - все это, безусловно, играет свою роль в
формировании профессионального музыканта. Но когда слышишь эту виртуозную игру и видишь эту чудесную девочку, не
можешь отделаться от мысли, что есть в этом какая-то таинственная, непознанная сила. И дана она этому хрупкому
созданию именно для того, чтобы нести нам через музыку свет и добро.
<br><br>
Публика не отпускала Еву очень долго. Она снова и снова садилась за рояль. А зал продолжал и продолжал аплодировать.
Из тех произведений, которые Ева сыграла на «бис», чаще всего звучал польский композитор Фредерик Шопен. В октябре
Ева планирует участвовать в Международном конкурсе пианистов имени Фредерика Шопена в Варшаве. Так что сахалинцы
стали первыми ценителями ее конкурсной программы.
