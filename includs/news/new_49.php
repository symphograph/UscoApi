В продолжение XXII концертного сезона «БЕТХОВЕН-250», посвященного творчеству Людвига ван Бетховена 25 мая 2021 года
на сцене МТЦ «Чехов-центр» Южно-Сахалинский камерный оркестр под руководством художественного руководителя и
главного дирижера Тиграна Ахназаряна представил вниманию жителей и гостей города Южно- Сахалинска очередной концерт,
посвященный этому гениальному композитору.
<br><br>
