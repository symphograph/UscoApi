<br><br>
Особенным гостем концерта стала солистка Ева Геворгян - победитель целого ряда международных конкурсов, лауреат
Международного конкурса Cleveland International Piano Competition, обладатель премии International Classical
Music Awards, которая прилетела из Москвы, чтобы порадовать слушателей удивительной игрой на фортепиано.
Блистательная солистка, сыгравшая с ведущими симфоническими оркестрами в самых престижных концертных залах мира,
раскрыла сахалинцам всю мощь своего таланта.
<br><br>
<blockquote>
    - Сегодня вы услышите, как чудесные звуки этого великолепного рояля наполнят зал, - сказал, открывая концерт, Тигран
    Ахназарян.<br>
    - Вы услышите великого Бетховена в исполнении одной из лучших пианисток молодого поколения.
</blockquote>
<br><br>
По словам маэстро, исполнение в сахалинском зале третьего концерта Людвига ван Бетховена посвящено 250-летию гения
классической музыки, юбилей которого из-за пандемии коронавирусной инфекции музыкальный мир продолжает отмечать и в
этом году. По той же причине был отменен концерт Евы Геворгян в Японии, хотя страна восходящего солнца знает и ждет
российскую пианистку в своих концертных залах. В этом году в Японии ей присвоили престижное звание «Молодой артист
фирмы Yamaha».
<br><br>
Привнеся в эту музыку «поэтическую идею», Бетховен обусловил еще большую слитность партий фортепиано с оркестром.
Страсть борьбы и покой созерцания, патетика и лиризм — эти полярные состояния были объединены методом симфонического
развития.
