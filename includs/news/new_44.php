<br><br>
Музыкальные произведения исполнил Южно-Сахалинский камерный оркестр под руководством художественного руководителя и главного дирижера Тиграна Ахназаряна совместно со сводным хором Сахалинского колледжа искусств и Сахалинской филармонии El Cantо.
<br><br>
