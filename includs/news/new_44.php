Праздничный концерт "Пасхальные встречи 2021" в рамках общегородского творческого проекта "Культурная столица" прошёл в Южно-Сахалинске.
<br><br>
С приветственными словами к собравшимся обратились архиепископ Южно-Сахалинский и Курильский Никанор и мэр Южно-Сахалинска Сергей Надсадин.
<br><br>
