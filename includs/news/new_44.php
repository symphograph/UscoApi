<br><br>
В программе — исполнение произведений классической музыки: хор из оперы "Набукко" Дж. Верди, симфония №6 "Пасторальная" Людвига ван Бетховена и др.
Специальным приглашенным гостем "Пасхальных встреч" стал Федор Освер — гобоист из Москвы, участник и победитель различных российских и международных музыкальных конкурсов.
<br><br>
Цель ежегодного проекта "Пасхальные встречи" — приобщение горожан к духовным ценностям, заложенным в традициях Великого праздника Пасхи.
