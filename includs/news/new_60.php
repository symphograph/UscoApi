    Онлайн-курс «Art Team 2.0: эффективная реализация проекта» доступен с 29 апреля. Каждые два дня на платформе будет
    публиковаться новый видео-урок. По окончании каждого занятия слушателя ждут домашние задания, за выполнением которых
    будут следить эксперты-наставники – преподавательский состав курса. Для заданий, предусматривающих работу в команде,
    зарегистрированным участникам предоставляется доступ в закрытый чат. По итогам обучения слушатели получат
    электронный сертификат об успешном прохождении онлайн-курса.
    <br><br>


