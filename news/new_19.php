    обороны РФ, Министерства иностранных дел РФ, Министерства культуры
    РФ, общественных объединений, организаций зарубежных
    соотечественников. По итогам слушаний была принята резолюция,
    в которой рекомендовано одобрить Народный проект «Установление судеб
    пропавших без вести защитников Отечества» как эффективную форму
    патриотического воспитания.
    Первым регионом, где системно начали разработку специального
    проекта по установлению судеб, стала Рязанская область. В феврале 2018
    года губернатор региона Николай Любимов подписал с Фондом
    «Народный проект» соглашение о реализации одноименного проекта на
    территории Рязанской области. Большой интерес людей к судьбе отцов,
    дедов и прадедов, активное участие школьников, добровольцев,
    общественности, а также новые технологии, которые позволяют взглянуть
    на архивные документы того времени, сегодня дали возможность всем нам
    отдать дань памяти своим предкам, вернуть пропавших без вести бойцов
    их родным семьям.
    В 2018 году Фонд «Народный проект» привлек к реализации
    проекта наших соотечественников, проживающих за рубежом, в том числе
    координаторов Бессмертного полка. Именно живая история, которая
    создается и восстанавливается благодаря каждому участнику Народного
    проекта «Установление судеб пропавших без вести защитников
    Отечества», где бы он ни жил, в какой стране мира не находился, способна
    восстановить правду и сохранить историю.
    4 марта 2018 года в Москве на Международном форуме «Вместе с
    Россией», организованным фондом «Русский мир» и собравшим
    представителей российского народа из 84 стран, был презентован
    Народный проект «Установление судеб пропавших без вести защитников
    Отечества». В обсуждении приняли участие представители российских и
    зарубежных общественных организаций и средств массовой информации,
    представляющие такие страны, как Россия, Дания, Швейцария, Панама,
    Азербайджан, Италия, ОАЭ, Сербия, Исландия, Нидерланды, Армения,
    Турция, Китай, Бразилия и другие.
    10 мая 2018 года в Государственной Думе состоялось открытие
    выставки «Установление судеб пропавших без вести защитников
    Отечества. Бессмертный полк». Министр труда и социальной защиты
    населения Рязанской области Валерий Емец рассказал, что Рязанская
    область стала первым регионом, в котором реализуется Народный проект
    «Установление судеб пропавших без вести защитников Отечества».
    Выставка организована при содействии Комитета Госдумы по
