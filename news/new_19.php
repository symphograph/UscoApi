    Фонд «Народный проект» совместно с партнерскими
    организациями осуществляет деятельность по реализации Народного
    проекта «Установление судеб пропавших без вести защитников
    Отечества». Проведены десятки обсуждений Народного проекта в Москве,
    Нижнем Новгороде, Пензе, Орле, Санкт-Петербурге, Барнауле, Тюмени,
    Липецке, Курске, Майкопе, Воронеже и других городах. В регионах
    страны прошли соответствующие общественные слушания и круглые
    столы.
    Необходимость проекта возникла ввиду заинтересованности и
    желания граждан, прежде всего, молодежи, получить информацию о своих
    родственниках - участниках Великой Отечественной войны, погибших или
    пропавших без вести в годы войны. Статистика обращений на Горячую
    линию Бессмертного полка показала, что жители нашей страны всё
    активнее интересуются корнями своей семьи и судьбами военного
    поколения, порядка 70% звонков – это заявки на установление судеб
    участников Великой Отечественной войны.
    Фонд «Народный проект» при взаимодействии с ведущими
    архивами страны (Центральный архив Министерства обороны Российской
    Федерации, Росархив и др.) восстанавливает военные судьбы, боевой путь
    участников Великой Отечественной войны по запросам родственников.
    Исследовательскую работу проводят специалисты и добровольцы
    «Народного проекта», осуществляющие поиск информации по
    собственным методикам на десятках Интернет-ресурсах и путем запросов
    в российские и зарубежные архивы.
    В 2017 году в Государственной Думе прошли парламентские
    слушания «Патриотическое воспитание граждан России:
    «Бессмертный полк». Мероприятие организовано Комитетом
    Государственной Думы по образованию и науке совместно с Комитетом по
    обороне и Комитетом по труду, социальной политике и делам ветеранов. В
    работе парламентских слушаний приняли участие депутаты Госдумы,
    члены Совета Федерации, представители законодательных и высших
    исполнительных органов государственной власти субъектов Российской
    2
