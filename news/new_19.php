    без вести защитников Отечества» был презентован на площадке женевской
    штаб-квартиры ООН на II Международной конференции
    «Противодействие героизации нацизма и неонацизму». В конференции
    приняли участие координаторы шествия Бессмертного полка и
    представители дипломатических миссий таких государств, как Россия,
    Белоруссия, Израиль, Польша, Армения, Грузия, Швейцария, Турция,
    Сербия, Алжир, КНДР, Бразилия, Ирак, Венесуэла, Таджикистан,
    Никарагуа, Кипр, Туркмения, Азербайджан и Китай и другие.
    В 2019 году Народный проект «Установление судеб пропавших без
    вести защитников Отечества» получил широкую информационную
    поддержку как федеральных, так и региональных СМИ. Исполнительный
    директор Фонда «Народный проект» Елена Калгина вошла в
    попечительский совет телеканала «ПОБЕДА», который пополнил
    «Цифровое телесемейство» Первого канала.
    В марте 2020 года, при поддержке Фонда президентских грантов,
    начал свою деятельность Всероссийский центр приема и обработки
    обращений граждан Российской Федерации на поиск информации о
    погибших и пропавших без вести родственниках в годы Великой
    Отечественной войны - Народный проект «Установление судеб погибших
    и пропавших без вести защитников Отечества».
    В апреле создана автоматизированная информационная система –
    онлайн-сервис «Найти солдата» (poisk.proektnaroda.ru), обеспечивающая
